#!/usr/bin/env node

import { execFileSync } from 'node:child_process';
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { verifyDefinitionFiles } from './cpds-verifier.mjs';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const definitionsDir = optionValue('--definitions-dir') || join(root, 'specs/definitions');
const jsOutput = optionValue('--js-output') || join(root, 'packages/js/src/builtin-definitions.js');
const goOutput = optionValue('--go-output') || join(root, 'packages/go/builtin_definitions.go');
const check = process.argv.includes('--check');

const verification = await verifyDefinitionFiles(definitionsDir);
if (verification.errors.length > 0) {
  for (const error of verification.errors) {
    console.error(`${error.file}: ${error.message}`);
  }
  throw new Error(`invalid CPDS definitions: ${verification.errors.length} error(s)`);
}
const definitions = verification.definitions;

const jsText = renderJS(definitions);
const goText = renderGo(definitions);

if (check) {
  await assertCurrent(jsOutput, jsText);
  await assertCurrent(goOutput, goText);
} else {
  await writeFile(jsOutput, jsText);
  await writeFile(goOutput, goText);
}

function renderJS(value) {
  return `// Code generated by tools/generate-definitions.mjs. DO NOT EDIT.\n\n/** @type {ReadonlyArray<import('./index.js').ConnparseDefinition>} */\nexport const builtInDefinitions = Object.freeze(${JSON.stringify(value, null, 2)});\n`;
}

function renderGo(value) {
  const body = value.map((definition) => indent(renderGoDefinition(definition), 2)).join(',\n');
  const source = `// Code generated by tools/generate-definitions.mjs. DO NOT EDIT.\n\npackage connparse\n\nfunc BuiltInDefinitions() []Definition {\n\treturn []Definition{\n${body},\n\t}\n}\n`;
  return execFileSync('gofmt', [], { input: source, encoding: 'utf8' });
}

async function assertCurrent(path, expected) {
  const actual = await readFile(path, 'utf8');
  if (actual !== expected) {
    throw new Error(`${relative(path)} is out of date; run pnpm generate:definitions`);
  }
}

function relative(path) {
  return path.replace(`${root}/`, '');
}

function optionValue(name) {
  const index = process.argv.indexOf(name);
  if (index === -1) return null;
  const value = process.argv[index + 1];
  if (!value || value.startsWith('--')) {
    throw new Error(`${name} requires a value`);
  }
  return value;
}

function renderGoDefinition(definition) {
  const lines = ['Definition{'];
  field(lines, 'ID', goString(definition.id));
  field(lines, 'Name', goString(definition.name));
  field(lines, 'Type', goString(definition.type));
  field(lines, 'Schemes', goStringSlice(definition.schemes));
  field(lines, 'Adapter', goString(definition.adapter));
  if ('defaults' in definition) field(lines, 'Defaults', goAnyMap(definition.defaults));
  if ('authority' in definition) field(lines, 'Authority', goAnyMap(definition.authority));
  field(lines, 'ResourceRule', goRule(definition.resource));
  field(lines, 'PathRule', goRule(definition.path));
  if ('credentials' in definition) field(lines, 'CredentialsRule', goAnyMap(definition.credentials));
  field(lines, 'QueryParameters', goQueryRules(definition.query_parameters));
  field(lines, 'Validation', goValidation(definition.validation));
  if ('options' in definition) field(lines, 'Options', goAnyMap(definition.options));
  if ('redaction' in definition) field(lines, 'Redaction', goRedaction(definition.redaction));
  lines.push('}');
  return lines.join('\n');
}

function field(lines, name, value) {
  lines.push(`\t${name}: ${value},`);
}

function goRule(rule = {}) {
  const lines = ['Rule{'];
  if (rule.type) lines.push(`\tType: ${goString(rule.type)},`);
  if (rule.required) lines.push('\tRequired: true,');
  lines.push('}');
  return lines.join('\n');
}

function goValidation(validation = {}) {
  const lines = ['ValidationRule{'];
  if (validation.require_host) lines.push('\tRequireHost: true,');
  if (validation.port_range) {
    lines.push(`\tPortRange: ${goPortRange(validation.port_range)},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goPortRange(range = {}) {
  const lines = ['PortRange{'];
  if (range.min) lines.push(`\tMin: ${range.min},`);
  if (range.max) lines.push(`\tMax: ${range.max},`);
  lines.push('}');
  return lines.join('\n');
}

function goQueryRules(rules = {}) {
  const keys = Object.keys(rules).sort();
  if (keys.length === 0) return 'map[string]QueryRule{}';
  const lines = ['map[string]QueryRule{'];
  for (const key of keys) {
    lines.push(`\t${goString(key)}: ${goQueryRule(rules[key])},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goQueryRule(rule = {}) {
  const lines = ['QueryRule{'];
  if (rule.type) lines.push(`\tType: ${goString(rule.type)},`);
  if (Array.isArray(rule.allowed) && rule.allowed.length > 0) {
    lines.push(`\tAllowed: ${goAnySlice(rule.allowed)},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goRedaction(redaction = {}) {
  const lines = ['RedactionRule{'];
  if (Array.isArray(redaction.safe_credentials)) {
    lines.push(`\tSafeCredentials: ${goStringSlice(redaction.safe_credentials)},`);
  }
  if (Array.isArray(redaction.sensitive_keys)) {
    lines.push(`\tSensitiveKeys: ${goStringSlice(redaction.sensitive_keys)},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goAnyMap(value = {}) {
  const entries = Object.entries(value).sort(([a], [b]) => a.localeCompare(b));
  if (entries.length === 0) return 'map[string]any{}';
  const lines = ['map[string]any{'];
  for (const [key, item] of entries) {
    lines.push(`\t${goString(key)}: ${goAny(item)},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goStringSlice(value = []) {
  if (value.length === 0) return '[]string{}';
  return `[]string{${value.map(goString).join(', ')}}`;
}

function goAnySlice(value = []) {
  if (value.length === 0) return '[]any{}';
  return `[]any{${value.map(goAny).join(', ')}}`;
}

function goAny(value) {
  if (typeof value === 'string') return goString(value);
  if (typeof value === 'number') return String(value);
  if (typeof value === 'boolean') return value ? 'true' : 'false';
  if (Array.isArray(value)) return goAnySlice(value);
  if (value && typeof value === 'object') return goAnyMap(value);
  if (value == null) return 'nil';
  throw new Error(`unsupported Go literal value: ${String(value)}`);
}

function goString(value) {
  return JSON.stringify(value);
}

function indent(value, depth) {
  const prefix = '\t'.repeat(depth);
  return value
    .split('\n')
    .map((line) => (line ? `${prefix}${line}` : line))
    .join('\n');
}

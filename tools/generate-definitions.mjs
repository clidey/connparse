#!/usr/bin/env node

import { execFileSync } from 'node:child_process';
import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { verifyDefinitionFiles } from './cpds-verifier.mjs';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const definitionsDir = optionValue('--definitions-dir') || join(root, 'specs/definitions');
const jsOutput = optionValue('--js-output') || join(root, 'packages/js/src/builtin-definitions.js');
const goOutput = optionValue('--go-output') || join(root, 'packages/go/builtin_definitions.go');
const javaOutput = optionValue('--java-output') || join(root, 'packages/java/src/main/java/com/clidey/connparse/BuiltInDefinitions.java');
const pythonOutput = optionValue('--python-output') || join(root, 'packages/python/src/connparse/builtin_definitions.py');
const rustOutput = optionValue('--rust-output') || join(root, 'packages/rust/src/builtin_definitions.rs');
const check = process.argv.includes('--check');

const verification = await verifyDefinitionFiles(definitionsDir);
if (verification.errors.length > 0) {
  for (const error of verification.errors) {
    console.error(`${error.file}: ${error.message}`);
  }
  throw new Error(`invalid CPDS definitions: ${verification.errors.length} error(s)`);
}
const definitions = verification.definitions;

const jsText = renderJS(definitions);
const goText = renderGo(definitions);
const javaText = renderJava(definitions);
const pythonText = renderPython(definitions);
const rustText = renderRust(definitions);

if (check) {
  await assertCurrent(jsOutput, jsText);
  await assertCurrent(goOutput, goText);
  await assertCurrent(javaOutput, javaText);
  await assertCurrent(pythonOutput, pythonText);
  await assertCurrent(rustOutput, rustText);
} else {
  await writeFile(jsOutput, jsText);
  await writeFile(goOutput, goText);
  await writeFile(javaOutput, javaText);
  await writeFile(pythonOutput, pythonText);
  await writeFile(rustOutput, rustText);
}

function renderJS(value) {
  return `// Code generated by tools/generate-definitions.mjs. DO NOT EDIT.\n\n/** @type {ReadonlyArray<import('./index.js').ConnparseDefinition>} */\nexport const builtInDefinitions = Object.freeze(${JSON.stringify(value, null, 2)});\n`;
}

function renderGo(value) {
  const body = value.map((definition) => indent(renderGoDefinition(definition), 2)).join(',\n');
  const source = `// Code generated by tools/generate-definitions.mjs. DO NOT EDIT.\n\npackage connparse\n\nfunc BuiltInDefinitions() []Definition {\n\treturn []Definition{\n${body},\n\t}\n}\n`;
  return execFileSync('gofmt', [], { input: source, encoding: 'utf8' });
}

function renderPython(value) {
  return `# Code generated by tools/generate-definitions.mjs. DO NOT EDIT.\n\nfrom __future__ import annotations\n\nfrom .types import ConnparseDefinition\n\nBUILT_IN_DEFINITIONS: list[ConnparseDefinition] = ${pythonLiteral(value)}\n`;
}

function renderJava(value) {
  const body = value.map((definition) => indentSpaces(renderJavaDefinition(definition), 3)).join(',\n');
  return `// Code generated by tools/generate-definitions.mjs. DO NOT EDIT.\n\npackage com.clidey.connparse;\n\nimport java.util.AbstractMap;\nimport java.util.LinkedHashMap;\nimport java.util.List;\nimport java.util.Map;\n\nfinal class BuiltInDefinitions {\n    private BuiltInDefinitions() {}\n\n    static List<Definition> builtInDefinitions() {\n        return List.of(\n${body}\n        );\n    }\n\n    @SafeVarargs\n    private static Map<String, Object> map(Map.Entry<String, Object>... entries) {\n        Map<String, Object> output = new LinkedHashMap<>();\n        for (Map.Entry<String, Object> entry : entries) output.put(entry.getKey(), entry.getValue());\n        return output;\n    }\n\n    @SafeVarargs\n    private static Map<String, Definition.QueryRule> queryRules(Map.Entry<String, Definition.QueryRule>... entries) {\n        Map<String, Definition.QueryRule> output = new LinkedHashMap<>();\n        for (Map.Entry<String, Definition.QueryRule> entry : entries) output.put(entry.getKey(), entry.getValue());\n        return output;\n    }\n\n    private static Map.Entry<String, Object> entry(String key, Object value) {\n        return new AbstractMap.SimpleImmutableEntry<>(key, value);\n    }\n\n    private static Map.Entry<String, Definition.QueryRule> queryRule(String key, Definition.QueryRule value) {\n        return new AbstractMap.SimpleImmutableEntry<>(key, value);\n    }\n}\n`;
}

function renderRust(value) {
  const body = value.map((definition) => indent(renderRustDefinition(definition), 2)).join(',\n');
  const source = `// Code generated by tools/generate-definitions.mjs. DO NOT EDIT.\n\nuse serde_json::{json, Map, Value};\nuse std::collections::BTreeMap;\n\nuse crate::{Definition, PortRange, QueryRule, RedactionRule, Rule, ValidationRule};\n\npub fn built_in_definitions() -> Vec<Definition> {\n    vec![\n${body},\n    ]\n}\n\nfn object<const N: usize>(entries: [(&'static str, Value); N]) -> Map<String, Value> {\n    entries.into_iter().map(|(key, value)| (key.to_string(), value)).collect()\n}\n\nfn query_rules<const N: usize>(entries: [(&'static str, QueryRule); N]) -> BTreeMap<String, QueryRule> {\n    entries.into_iter().map(|(key, value)| (key.to_string(), value)).collect()\n}\n\nfn strings<const N: usize>(items: [&'static str; N]) -> Vec<String> {\n    items.into_iter().map(str::to_string).collect()\n}\n`;
  return execFileSync('rustfmt', ['--edition', '2024'], { input: source, encoding: 'utf8' });
}

async function assertCurrent(path, expected) {
  const actual = await readFile(path, 'utf8');
  if (actual !== expected) {
    throw new Error(`${relative(path)} is out of date; run pnpm generate:definitions`);
  }
}

function relative(path) {
  return path.replace(`${root}/`, '');
}

function optionValue(name) {
  const index = process.argv.indexOf(name);
  if (index === -1) return null;
  const value = process.argv[index + 1];
  if (!value || value.startsWith('--')) {
    throw new Error(`${name} requires a value`);
  }
  return value;
}

function renderGoDefinition(definition) {
  const lines = ['Definition{'];
  field(lines, 'ID', goString(definition.id));
  field(lines, 'Name', goString(definition.name));
  field(lines, 'Type', goString(definition.type));
  field(lines, 'Schemes', goStringSlice(definition.schemes));
  field(lines, 'Adapter', goString(definition.adapter));
  if ('defaults' in definition) field(lines, 'Defaults', goAnyMap(definition.defaults));
  if ('authority' in definition) field(lines, 'Authority', goAnyMap(definition.authority));
  field(lines, 'ResourceRule', goRule(definition.resource));
  field(lines, 'PathRule', goRule(definition.path));
  if ('credentials' in definition) field(lines, 'CredentialsRule', goAnyMap(definition.credentials));
  field(lines, 'QueryParameters', goQueryRules(definition.query_parameters));
  field(lines, 'Validation', goValidation(definition.validation));
  if ('options' in definition) field(lines, 'Options', goAnyMap(definition.options));
  if ('redaction' in definition) field(lines, 'Redaction', goRedaction(definition.redaction));
  lines.push('}');
  return lines.join('\n');
}

function field(lines, name, value) {
  lines.push(`\t${name}: ${value},`);
}

function goRule(rule = {}) {
  const lines = ['Rule{'];
  if (rule.type) lines.push(`\tType: ${goString(rule.type)},`);
  if (rule.required) lines.push('\tRequired: true,');
  lines.push('}');
  return lines.join('\n');
}

function goValidation(validation = {}) {
  const lines = ['ValidationRule{'];
  if (validation.require_host) lines.push('\tRequireHost: true,');
  if (validation.port_range) {
    lines.push(`\tPortRange: ${goPortRange(validation.port_range)},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goPortRange(range = {}) {
  const lines = ['PortRange{'];
  if (range.min) lines.push(`\tMin: ${range.min},`);
  if (range.max) lines.push(`\tMax: ${range.max},`);
  lines.push('}');
  return lines.join('\n');
}

function goQueryRules(rules = {}) {
  const keys = Object.keys(rules).sort();
  if (keys.length === 0) return 'map[string]QueryRule{}';
  const lines = ['map[string]QueryRule{'];
  for (const key of keys) {
    lines.push(`\t${goString(key)}: ${goQueryRule(rules[key])},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goQueryRule(rule = {}) {
  const lines = ['QueryRule{'];
  if (rule.type) lines.push(`\tType: ${goString(rule.type)},`);
  if (Array.isArray(rule.allowed) && rule.allowed.length > 0) {
    lines.push(`\tAllowed: ${goAnySlice(rule.allowed)},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goRedaction(redaction = {}) {
  const lines = ['RedactionRule{'];
  if (Array.isArray(redaction.safe_credentials)) {
    lines.push(`\tSafeCredentials: ${goStringSlice(redaction.safe_credentials)},`);
  }
  if (Array.isArray(redaction.sensitive_keys)) {
    lines.push(`\tSensitiveKeys: ${goStringSlice(redaction.sensitive_keys)},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goAnyMap(value = {}) {
  const entries = Object.entries(value).sort(([a], [b]) => a.localeCompare(b));
  if (entries.length === 0) return 'map[string]any{}';
  const lines = ['map[string]any{'];
  for (const [key, item] of entries) {
    lines.push(`\t${goString(key)}: ${goAny(item)},`);
  }
  lines.push('}');
  return lines.join('\n');
}

function goStringSlice(value = []) {
  if (value.length === 0) return '[]string{}';
  return `[]string{${value.map(goString).join(', ')}}`;
}

function goAnySlice(value = []) {
  if (value.length === 0) return '[]any{}';
  return `[]any{${value.map(goAny).join(', ')}}`;
}

function goAny(value) {
  if (typeof value === 'string') return goString(value);
  if (typeof value === 'number') return String(value);
  if (typeof value === 'boolean') return value ? 'true' : 'false';
  if (Array.isArray(value)) return goAnySlice(value);
  if (value && typeof value === 'object') return goAnyMap(value);
  if (value == null) return 'nil';
  throw new Error(`unsupported Go literal value: ${String(value)}`);
}

function goString(value) {
  return JSON.stringify(value);
}

function pythonLiteral(value, depth = 0) {
  if (typeof value === 'string') return JSON.stringify(value);
  if (typeof value === 'number') return String(value);
  if (typeof value === 'boolean') return value ? 'True' : 'False';
  if (value == null) return 'None';
  if (Array.isArray(value)) {
    if (value.length === 0) return '[]';
    const inner = value.map((item) => `${'  '.repeat(depth + 1)}${pythonLiteral(item, depth + 1)},`).join('\n');
    return `[\n${inner}\n${'  '.repeat(depth)}]`;
  }
  if (typeof value === 'object') {
    const entries = Object.entries(value).sort(([a], [b]) => a.localeCompare(b));
    if (entries.length === 0) return '{}';
    const inner = entries
      .map(([key, item]) => `${'  '.repeat(depth + 1)}${JSON.stringify(key)}: ${pythonLiteral(item, depth + 1)},`)
      .join('\n');
    return `{\n${inner}\n${'  '.repeat(depth)}}`;
  }
  throw new Error(`unsupported Python literal value: ${String(value)}`);
}

function renderJavaDefinition(definition) {
  return `new Definition(\n    ${javaString(definition.id)},\n    ${javaString(definition.name)},\n    ${javaString(definition.type)},\n    ${javaStringList(definition.schemes)},\n    ${javaString(definition.adapter || '')},\n    ${javaObject(definition.defaults)},\n    ${javaObject(definition.authority)},\n    ${javaRule(definition.resource)},\n    ${javaRule(definition.path)},\n    ${javaObject(definition.credentials)},\n    ${javaQueryRules(definition.query_parameters)},\n    ${javaValidation(definition.validation)},\n    ${javaObject(definition.options)},\n    ${javaRedaction(definition.redaction)}\n)`;
}

function javaRule(rule = {}) {
  return `new Definition.Rule(${javaString(rule.type || '')}, ${javaBool(Boolean(rule.required))})`;
}

function javaQueryRules(rules = {}) {
  const entries = Object.entries(rules).sort(([a], [b]) => a.localeCompare(b));
  if (entries.length === 0) return 'queryRules()';
  return `queryRules(${entries.map(([key, rule]) => `queryRule(${javaString(key)}, ${javaQueryRule(rule)})`).join(', ')})`;
}

function javaQueryRule(rule = {}) {
  return `new Definition.QueryRule(${javaString(rule.type || '')}, ${javaObjectList(rule.allowed || [])})`;
}

function javaValidation(validation = {}) {
  const portRange = validation?.port_range
    ? `new Definition.PortRange(${javaNumber(validation.port_range.min || 0)}, ${javaNumber(validation.port_range.max || 0)})`
    : 'null';
  return `new Definition.ValidationRule(${javaBool(Boolean(validation?.require_host))}, ${portRange})`;
}

function javaRedaction(redaction = {}) {
  return `new Definition.RedactionRule(${javaStringList(redaction.safe_credentials || [])}, ${javaStringList(redaction.sensitive_keys || [])})`;
}

function javaObject(value = {}) {
  const entries = Object.entries(value || {}).sort(([a], [b]) => a.localeCompare(b));
  if (entries.length === 0) return 'map()';
  return `map(${entries.map(([key, item]) => `entry(${javaString(key)}, ${javaValue(item)})`).join(', ')})`;
}

function javaObjectList(value = []) {
  if (!value.length) return 'List.of()';
  return `List.of(${value.map(javaValue).join(', ')})`;
}

function javaStringList(value = []) {
  if (!value.length) return 'List.of()';
  return `List.of(${value.map(javaString).join(', ')})`;
}

function javaValue(value) {
  if (typeof value === 'string') return javaString(value);
  if (typeof value === 'number') return javaNumber(value);
  if (typeof value === 'boolean') return javaBool(value);
  if (Array.isArray(value)) return javaObjectList(value);
  if (value && typeof value === 'object') return javaObject(value);
  if (value == null) return 'null';
  throw new Error(`unsupported Java literal value: ${String(value)}`);
}

function javaNumber(value) {
  return Number.isInteger(value) ? `${value}L` : String(value);
}

function javaString(value) {
  return JSON.stringify(value);
}

function javaBool(value) {
  return value ? 'true' : 'false';
}

function renderRustDefinition(definition) {
  return `Definition {\n    id: ${rustString(definition.id)},\n    name: ${rustString(definition.name)},\n    kind: ${rustString(definition.type)},\n    schemes: ${rustStringVec(definition.schemes)},\n    adapter: ${rustString(definition.adapter || '')},\n    defaults: ${rustObject(definition.defaults)},\n    authority: ${rustObject(definition.authority)},\n    resource: ${rustRule(definition.resource)},\n    path: ${rustRule(definition.path)},\n    credentials: ${rustObject(definition.credentials)},\n    query_parameters: ${rustQueryRules(definition.query_parameters)},\n    validation: ${rustValidation(definition.validation)},\n    options: ${rustObject(definition.options)},\n    redaction: ${rustRedaction(definition.redaction)},\n}`;
}

function rustRule(rule = {}) {
  return `Rule { kind: ${rustString(rule.type || '')}, required: ${rustBool(Boolean(rule.required))} }`;
}

function rustQueryRules(rules = {}) {
  const entries = Object.entries(rules).sort(([a], [b]) => a.localeCompare(b));
  if (entries.length === 0) return 'query_rules([])';
  return `query_rules([${entries
    .map(([key, rule]) => `(${rustStr(key)}, ${rustQueryRule(rule)})`)
    .join(', ')}])`;
}

function rustQueryRule(rule = {}) {
  return `QueryRule { kind: ${rustString(rule.type || '')}, allowed: ${rustValueVec(rule.allowed || [])} }`;
}

function rustValidation(validation = {}) {
  const portRange = validation.port_range
    ? `Some(PortRange { min: ${validation.port_range.min || 0}, max: ${validation.port_range.max || 0} })`
    : 'None';
  return `ValidationRule { require_host: ${rustBool(Boolean(validation.require_host))}, port_range: ${portRange} }`;
}

function rustRedaction(redaction = {}) {
  return `RedactionRule { safe_credentials: ${rustStringVec(redaction.safe_credentials || [])}, sensitive_keys: ${rustStringVec(redaction.sensitive_keys || [])} }`;
}

function rustObject(value = {}) {
  const entries = Object.entries(value).sort(([a], [b]) => a.localeCompare(b));
  if (entries.length === 0) return 'object([])';
  return `object([${entries.map(([key, item]) => `(${rustStr(key)}, ${rustValue(item)})`).join(', ')}])`;
}

function rustValueVec(value = []) {
  if (value.length === 0) return 'Vec::new()';
  return `vec![${value.map(rustValue).join(', ')}]`;
}

function rustStringVec(value = []) {
  if (value.length === 0) return 'strings([])';
  return `strings([${value.map(rustStr).join(', ')}])`;
}

function rustValue(value) {
  return `json!(${JSON.stringify(value)})`;
}

function rustString(value) {
  return `${rustStr(value)}.to_string()`;
}

function rustStr(value) {
  return JSON.stringify(value);
}

function rustBool(value) {
  return value ? 'true' : 'false';
}

function indentSpaces(value, depth) {
  const prefix = '    '.repeat(depth);
  return value
    .split('\n')
    .map((line) => (line ? `${prefix}${line}` : line))
    .join('\n');
}

function indent(value, depth) {
  const prefix = '\t'.repeat(depth);
  return value
    .split('\n')
    .map((line) => (line ? `${prefix}${line}` : line))
    .join('\n');
}
